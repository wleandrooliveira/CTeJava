package br.com.moveti.erpmove.transporte.cte.xml;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * informações dos containeres contidos no vagão - Não Obrigatório [0-n]
 * @author Jéssica
 */
@XStreamAlias("contVag")
public class contVag {
    @XStreamAlias("nCont")
    private String nCont;
    @XStreamAlias("dPrev")
    private String dPrev;

    /**
     * @return the nCont
     */
    public String getnCont() {
        return nCont;
    }

    /**
     * @param nCont the nCont to set
     */
    public void setnCont(String nCont) {
        this.nCont = nCont;
    }

    /**
     * @return the dPrev
     */
    public String getdPrev() {
        return dPrev;
    }

    /**
     * @param dPrev the dPrev to set
     */
    public void setdPrev(String dPrev) {
        this.dPrev = dPrev;
    }
}
