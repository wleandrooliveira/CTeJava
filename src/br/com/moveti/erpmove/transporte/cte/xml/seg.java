package br.com.moveti.erpmove.transporte.cte.xml;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * Informações de Seguro da Carga
 * @author Jéssica
 */
@XStreamAlias("seg")
public class seg {
    @XStreamAlias("respSeg")
    private String respSeg;
    @XStreamAlias("xSeg")
    private String xSeg;
    @XStreamAlias("nApol")    
    private String nApol;
    @XStreamAlias("nAver")
    private String nAver;
    @XStreamAlias("vCarga")
    private String vCarga;

    /**
     * @return the respSeg
     */
    public String getRespSeg() {
        return respSeg;
    }

    /**
     * @param respSeg the respSeg to set
     */
    public void setRespSeg(String respSeg) {
        this.respSeg = respSeg;
    }

    /**
     * @return the xSeg
     */
    public String getxSeg() {
        return xSeg;
    }

    /**
     * @param xSeg the xSeg to set
     */
    public void setxSeg(String xSeg) {
        this.xSeg = xSeg;
    }

    /**
     * @return the nApol
     */
    public String getnApol() {
        return nApol;
    }

    /**
     * @param nApol the nApol to set
     */
    public void setnApol(String nApol) {
        this.nApol = nApol;
    }

    /**
     * @return the nAver
     */
    public String getnAver() {
        return nAver;
    }

    /**
     * @param nAver the nAver to set
     */
    public void setnAver(String nAver) {
        this.nAver = nAver;
    }

    /**
     * @return the vCarga
     */
    public String getvCarga() {
        return vCarga;
    }

    /**
     * @param vCarga the vCarga to set
     */
    public void setvCarga(String vCarga) {
        this.vCarga = vCarga;
    }
}
