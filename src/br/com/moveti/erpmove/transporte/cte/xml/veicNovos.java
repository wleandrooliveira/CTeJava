package br.com.moveti.erpmove.transporte.cte.xml;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * informações dos veículos transportados - 
 * @author Jéssica
 */
@XStreamAlias("veicNovos")
public class veicNovos {
    @XStreamAlias("chassi")
    private String chassi;
    @XStreamAlias("cCor")
    private String cCor;
    @XStreamAlias("xCor")
    private String xCor;
    @XStreamAlias("cMod")    
    private String cMod;
    @XStreamAlias("vUnit")
    private String vUnit;
    @XStreamAlias("vFrete")
    private String vFrete;

    /**
     * @return the chassi
     */
    public String getChassi() {
        return chassi;
    }

    /**
     * @param chassi the chassi to set
     */
    public void setChassi(String chassi) {
        this.chassi = chassi;
    }

    /**
     * @return the cCor
     */
    public String getcCor() {
        return cCor;
    }

    /**
     * @param cCor the cCor to set
     */
    public void setcCor(String cCor) {
        this.cCor = cCor;
    }

    /**
     * @return the xCor
     */
    public String getxCor() {
        return xCor;
    }

    /**
     * @param xCor the xCor to set
     */
    public void setxCor(String xCor) {
        this.xCor = xCor;
    }

    /**
     * @return the cMod
     */
    public String getcMod() {
        return cMod;
    }

    /**
     * @param cMod the cMod to set
     */
    public void setcMod(String cMod) {
        this.cMod = cMod;
    }

    /**
     * @return the vUnit
     */
    public String getvUnit() {
        return vUnit;
    }

    /**
     * @param vUnit the vUnit to set
     */
    public void setvUnit(String vUnit) {
        this.vUnit = vUnit;
    }

    /**
     * @return the vFrete
     */
    public String getvFrete() {
        return vFrete;
    }

    /**
     * @param vFrete the vFrete to set
     */
    public void setvFrete(String vFrete) {
        this.vFrete = vFrete;
    }
}
