package br.com.moveti.erpmove.transporte.cte.xml;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * Informações dos demais documentos - Obrigatório [1-n]
 * @author Jéssica
 */
@XStreamAlias("infOutros")
public class infOutros {
    @XStreamAlias("tpDoc")
    private String tpDoc;
    //NO
    @XStreamAlias("descOutros")
    private String descOutros;
    //NO
    @XStreamAlias("nDoc")
    private String nDoc;
    //NO
    @XStreamAlias("dEmi")
    private String dEmi;
    //NO
    @XStreamAlias("vDocFisc")
    private String vDocFisc;

    /**
     * @return the tpDoc
     */
    public String getTpDoc() {
        return tpDoc;
    }

    /**
     * @param tpDoc the tpDoc to set
     */
    public void setTpDoc(String tpDoc) {
        this.tpDoc = tpDoc;
    }

    /**
     * @return the descOutros
     */
    public String getDescOutros() {
        return descOutros;
    }

    /**
     * @param descOutros the descOutros to set
     */
    public void setDescOutros(String descOutros) {
        this.descOutros = descOutros;
    }

    /**
     * @return the nDoc
     */
    public String getnDoc() {
        return nDoc;
    }

    /**
     * @param nDoc the nDoc to set
     */
    public void setnDoc(String nDoc) {
        this.nDoc = nDoc;
    }

    /**
     * @return the dEmi
     */
    public String getdEmi() {
        return dEmi;
    }

    /**
     * @param dEmi the dEmi to set
     */
    public void setdEmi(String dEmi) {
        this.dEmi = dEmi;
    }

    /**
     * @return the vDocFisc
     */
    public String getvDocFisc() {
        return vDocFisc;
    }

    /**
     * @param vDocFisc the vDocFisc to set
     */
    public void setvDocFisc(String vDocFisc) {
        this.vDocFisc = vDocFisc;
    }
}
